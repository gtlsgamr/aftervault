#!/bin/bash

# Install React Navigation core
npm install @react-navigation/native

# Install dependencies for React Navigation
npm install react-native-screens react-native-safe-area-context

# Install Bottom Tabs navigator
npm install @react-navigation/bottom-tabs

# Install vector icons for tab bar icons
npm install react-native-vector-icons
npm install @types/react-native-vector-icons --save-dev

echo "React Navigation packages installed successfully!"
